package org.dejava.component.util.accesscontrol.model;

/**
 * System functionality.
 */
public interface Functionality {

	/**
	 * Gets the identifier of the functionality.
	 * 
	 * @return The identifier of the functionality.
	 */
	String getIdentifier();

	/**
	 * Gets the name of the functionality.
	 * 
	 * @return The name of the functionality.
	 */
	String getName();

}
