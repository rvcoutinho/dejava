package org.dejava.component.i18n.message.handler.model;

/**
 * Message type in a application.
 */
public enum ApplicationMessageType {
	
	/**
	 * Fatal error message.
	 */
	FATAL,
	
	/**
	 * General error message.
	 */
	ERROR,
	
	/**
	 * Warning message.
	 */
	WARNING,
	
	/**
	 * Information message.
	 */
	INFORMATION;
}
